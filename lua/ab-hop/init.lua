local function map(mode, lhs, rhs, opts)
  local options = { noremap = true }
  if opts then
    options = vim.tbl_extend("force", options, opts)
  end
  vim.api.nvim_set_keymap(mode, lhs, rhs, options)
end

require'hop'.setup { keys = 'etovxqpdygfblzhckisuran', jump_on_sole_occurrence = false }


-- place this in one of your configuration file(s)
map("n", "s", "<cmd>lua require'hop'.hint_words()<cr>")
map("n", "S", "<cmd>lua require'hop'.hint_lines()<cr>")
map("v", "s", "<cmd>lua require'hop'.hint_words()<cr>")
map("v", "S", "<cmd>lua require'hop'.hint_lines()<cr>")

